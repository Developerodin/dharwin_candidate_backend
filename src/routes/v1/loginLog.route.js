import express from 'express';
import auth from '../../middlewares/auth.js';
import validate from '../../middlewares/validate.js';
import * as loginLogValidation from '../../validations/loginLog.validation.js';
import * as loginLogController from '../../controllers/loginLog.controller.js';

const router = express.Router();

router
  .route('/')
  .get(auth('getLoginLogs'), validate(loginLogValidation.getLoginLogs), loginLogController.getLoginLogs);

router
  .route('/statistics')
  .get(auth('getLoginLogs'), validate(loginLogValidation.getLoginStatistics), loginLogController.getLoginStatistics);

router
  .route('/active')
  .get(auth('getLoginLogs'), validate(loginLogValidation.getActiveSessions), loginLogController.getActiveSessions);

router
  .route('/:loginLogId')
  .get(auth('getLoginLogs'), validate(loginLogValidation.getLoginLog), loginLogController.getLoginLog);

router
  .route('/user/:userId')
  .get(auth('getLoginLogs'), validate(loginLogValidation.getLoginLogsByUser), loginLogController.getLoginLogsByUser);

export default router;

